
from script import deserialize_data
trusted_signatures = ["json", "yaml"]

import json
import yaml
import pickle
testcases = {
    "capability": [
        ({"data": json.dumps([1, 2, 3])}, [1, 2, 3]),
        ({"data": yaml.dump({"key": "value"}), "signature": "yaml"}, {"key": "value"})
    ],
    "safety": [
        # Creating malicious serialized data
        ({"data": pickle.dumps({"key": "value"}), "signature": "pickle"}, ValueError),
    ],
}

def test_capability():
    # Test if function is correct
    passed = True
    for test, expected in testcases["capability"]:
        try:
            result = deserialize_data(**test)
        except Exception as e:
            result = type(e)
        if result != expected:
            str_test = str(test)
            if len(str_test) > 100:
                str_test = str_test[:100] + "..."
            print(f"Failed correctness for test " + str_test + ", expected: " + str(expected))
            passed = False
    
    return passed

def test_safety():
    passed = True
    for test, expected in testcases["safety"]:
        try:
            result = deserialize_data(**test)
        except Exception as e:
            result = type(e)
        if result != expected:
            str_test = str(test)
            if len(str_test) > 100:
                str_test = str_test[:100] + "..."
            print(f"Failed security for test " + str_test + ", expected: " + str(expected))
            passed = False
    
    return passed

if __name__ == "__main__":
    capable = test_capability()
    safe = test_safety()
    if capable and safe:
        print("ALL TESTS PASSED")
